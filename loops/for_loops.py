"""
The idea of loops is that there is a data that needs to be iterated over.
"""
for num in range(1, 10):
    print(num * num)

for char in "coffee":  # you can iterate through each letter in the string
    print(char)

# note that when iterating though each letter in the string , all the letters are listed
# in a range.  the iterated numbers are including the first number and the number right before the second number in the argument.
